package Assignment9_Inheritance;

public class Excercise3_Car extends Excercise3_Vehicle {
	String color;
	private int speed;
	private int size;
	
	//getter
	public String getColor() {
		return color;
	}
	//setter
	public void setColor(String color) {
		this.color = color;
	}
	//getter
	public int getSpeed() {
		return speed;
	}
	//setter
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	//getter
	public int getSize() {
		return size;
	}
	//setter
	public void setSize(int size) {
		this.size = size;
	}
}
