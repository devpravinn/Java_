package Assignment9_Inheritance;

public class Excercise3_Vehicle {
	int CC;
	int gears;
	String color;
	
	//getters
	public String getColor() {
		return color;
	}
	public int getCC() {
		return CC;
	}
	public int getGears() {
		return gears;
	}
	
	//setters
	public void setColor(String color) {
		this.color = color;
	}
	public void setCC(int CC) {
		this.CC = CC;
	}
	public void setGears(int gears) {
		this.gears = gears;
	}
	
	
}
