package Assignment1_Simple_Basic_Assignment;

public class Excercise8 {
	//Playing with string, int, brackets and concatenation of two strings
	public static void main(String[] args) {
		int num1 = 22;
		int num2 = 8;
		
		System.out.println(num1+"+"+num2+"="+(num1+num2));
		System.out.println(num1+"-"+num2+"="+(num1-num2));
		System.out.println(num1+"*"+num2+"="+(num1*num2));
		System.out.println(num1+"/"+num2+"="+(num1/num2));
		System.out.println(num1+"%"+num2+"="+(num1%num2));
	}
}
