package Assignment1_Simple_Basic_Assignment;

public class Excercise5 {
	//Variable assignment, increment and printing
	public static void main(String[] args) {
		int a = 10;
		System.out.println(a);
		a=a+2;
		System.out.println(a);
		a=a*2;
		System.out.println(a);
		a=a-2;
		System.out.println(a);
	}
}
