public class Excercise7 {
//	Understanding variable printing with and without double quote
	public static void main(String[] args) {
		int x = 1, y = 2;
		System.out.println("x");
		System.out.println("y");
		System.out.println("x+y");
		System.out.println(x);
		System.out.println(y);
		System.out.println(x+y);
	}
}
